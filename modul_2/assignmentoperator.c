//assignment operator with C language
#include <stdio.h>
#include <conio.h>

int main(){
	int a=8, b=4, x=8, y=4;
	printf("\nNilai A   = %d", a);
	printf("\nNilai ++A = %d", ++a);
	printf("\nNilai A   = %d", a);
	printf("\nNilai B   = %d", b);
	printf("\nNilai --B = %d", --b);
	printf("\nNilai B   = %d", b);
	
	printf("\nNilai X   = %d", x);
	printf("\nNilai ++X = %d", ++x);
	printf("\nNilai X   = %d", x);
	printf("\nNilai Y   = %d", y);
	printf("\nNilai --Y = %d", --y);
	printf("\nNilai Y   = %d", y);
	
	getch();
}
